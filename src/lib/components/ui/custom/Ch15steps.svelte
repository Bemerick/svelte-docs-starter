					by requesting office with the last action in the employee's Official Personnel Folder to be
					sure they are correct.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>2</p>
			</td>
			<td width="558">
				<p>
					Use job aid, <strong>Effects of Nonpay Status</strong>, to identify actions necessary when
					an employee is placed in a nonpay status.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>3</p>
			</td>
			<td width="558">
				<p>
					Use Table 15-A to select the nature of action and authority. Put them in blocks 5A-F of
					the <a href="https://www.opm.gov/forms/pdf_fill/sf52.pdf">SF- 52</a>.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>4</p>
			</td>
			<td width="558">
				<p>
					Use Table 15-B to select remarks/remarks codes required by the Office of Personnel
					Management for the action. Enter them in Part F of the <a
						href="https://www.opm.gov/forms/pdf_fill/sf52.pdf">SF-52</a
					>.
				</p>
				<p>&nbsp;</p>
				<p>
					Also enter any additional remarks/remarks codes that are required by your agency's
					instructions or that are necessary to explain the action.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>5</p>
			</td>
			<td width="558">
				<p>
					Complete the <a href="https://www.opm.gov/forms/pdf_fill/sf52.pdf">SF-52</a> as required by
					instructions in Chapter 4 of this
				</p>
				<p>Guide.</p>
				<p>&nbsp;</p>
				<p>
					When a suspension is not to be imposed on consecutive workdays, explain in remarks the
					schedule for the days on which the suspension will be carried out. For example,
					&ldquo;suspension to be imposed on Monday - Wednesday of each week for a total of 45
					days.&rdquo;
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>6</p>
			</td>
			<td width="558">
				<p>
					Follow your agency's instructions to obtain approval signature in Part C, block 2 of the <a
						href="https://www.opm.gov/forms/pdf_fill/sf52.pdf">SF-52</a
					>.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>7</p>
			</td>
			<td width="558">
				<p>
					Follow instructions in Chapter 4 of this Guide to complete the <a
						href="https://www.opm.gov/forms/pdfimage/sf50.pdf">SF-50</a
					>. Follow your agency's instructions to have it signed or authenticated.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>8</p>
			</td>
			<td width="558">
				<p>
					Enter or update not-to-exceed date of action in any tickler system your agency uses. Use
					job aid, Effects of Nonpay Status, to identify any changes (for example, within-grade
					increase eligibility date) that will change as a result of the employee's nonpay status
					and make the necessary changes in your tickler system.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>9</p>
			</td>
			<td width="558">
				<p>
					Check <a
						href="https://www.opm.gov/policy-data-oversight/data-analysis-documentation/personnel-documentation/#url=Personnel-Recordkeeping-GPR"
						>The Guide to Personnel Recordkeeping</a
					> to decide if any of the documents submitted with or created in connection with the action
					should be filed on the right side of the employee's Official Personnel Folder. Follow your
					agency's instructions to dispose of those not filed in the Folder.
				</p>
				<p>&nbsp;</p>
			</td>
		</tr>
		<tr>
			<td width="66">
				<p>10</p>
			</td>
			<td width="558">
				<p>Issue any notices required for the action, including the following if appropriate:</p>
				<p>
					&mdash;When the employee will be in nonpay status for more than 7 consecutive days, give
					the employee a completed <a href="https://www.gsa.gov/system/files/SF_8.pdf"
						>SF-8, Notice to Federal Employee About Unemployment Insurance,</a
					> showing the full address of the payroll office where the individual's records are maintained.
				</p>
				<p>&nbsp;</p>
				<p>
					&mdash;When an employee enters active duty with the Armed Forces or another uniformed
					service:
				</p>
				<p>
					&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; provide the employee with information about
					any applicable restoration rights after service is completed and explain those rights or
					refer the employee to a specialist who can explain them. (See Uniformed Services
					Employment and Reemployment Rights Act (USERRA), as codified in <a
						href="https://uscode.house.gov/view.xhtml?hl=false&amp;edition=prelim&amp;req=granuleid%3AUSC-prelim-title38-chapter43-subchapter1-front&amp;f=treesort&amp;num=0&amp;saved=%7CKHRpdGxlOjM4IHNlY3Rpb246NDMwMSBlZGl0aW9uOnByZWxpbSkgT1IgKGdyYW51bGVpZDpVU0MtcHJlbGltLXRpdGxlMzgtc2VjdGlvbjQzMDEp%7CdHJlZXNvcnQ%3D%7C%7C0%7Cfalse%7Cprelim"
						>38 U.S. Code chapter 43</a
					>
					and regulated in
					<a
						href="https://www.ecfr.gov/current/title-5/chapter-I/subchapter-B/part-353/subpart-C/section-353.301"
						>5 CFR part 353</a
					>.)
				</p>
				<p>
					&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; if applicable, ask the employee to state in
					writing whether unused annual leave should be paid in a lump sum or held until employee
					returns. See <a
						href="https://uscode.house.gov/view.xhtml?req=(title:5%20section:5552%20edition:prelim)%20OR%20(granuleid:USC-prelim-title5-section5552)&amp;f=treesort&amp;edition=prelim&amp;num=0&amp;jumpTo=true"
						>5 U.S. Code 5552</a
					>
					and
					<a
						href="https://www.ecfr.gov/current/title-5/chapter-I/subchapter-B/part-550/subpart-L/section-550.1203"
						>5 CFR 550.1203(c)</a
					>.
				</p>
				<p>
					&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; if the employee is a member of the Reserve or
					National Guard who is called to active duty, work with the employee to determine if they
					are covered by the reservist differential provision in <a
						href="https://uscode.house.gov/view.xhtml?req=(title:5%20section:5538%20edition:prelim)%20OR%20(granuleid:USC-prelim-title5-section5538)&amp;f=treesort&amp;edition=prelim&amp;num=0&amp;jumpTo=true"
						>5 U.S. Code 5538</a
					> and, if they are covered, provide information about the program.
				</p>
